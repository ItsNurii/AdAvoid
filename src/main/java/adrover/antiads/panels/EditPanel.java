    /**
     * Applies the selected visual theme to the panel.
     *
     * @param darkMode {@code true} for dark theme, {@code false} for light
     * theme
     */
    public void applyTheme(boolean darkMode) {

        Color bg = darkMode ? AppColor.DARK_BG : AppColor.LIGHT_BG;
        Color panel = darkMode ? AppColor.DARK_PANEL : AppColor.LIGHT_PANEL;
        Color btn = darkMode ? AppColor.DARK_BUTTON : AppColor.LIGHT_BUTTON;
        Color text = darkMode ? AppColor.DARK_TEXT : AppColor.LIGHT_TEXT;

        // Fondo del panel
        setBackground(bg);

        // Labels
        jLabelTitle.setForeground(text);
        jLabelPath.setForeground(text);
        jLabelSpeed.setForeground(text);
        jLabelYtDlp.setForeground(text);

        // TextFields
        jTextFieldPath.setBackground(panel);
        jTextFieldPath.setForeground(text);

        jTextFieldYtDlp.setBackground(panel);
        jTextFieldYtDlp.setForeground(text);

        // Botones
        jButtonBrowse1.setBackground(btn);
        jButtonBrowse1.setForeground(text);
        jButtonBrowse2.setBackground(btn);
        jButtonBrowse2.setForeground(text);

        jButtonBack.setBackground(btn);
        jButtonBack.setForeground(text);

        repaint();
    }

    /**
     * Enables proportional automatic resizing of all panel components.
     * <p>
     * Component bounds are recalculated based on the original panel size.
     * </p>
     */
    private void enableAutoResize() {

        // Guardar bounds originales
        for (Component c : getComponents()) {
            baseBounds.put(c, c.getBounds());
        }

        addComponentListener(new ComponentAdapter() {
            @Override
            public void componentResized(ComponentEvent e) {

                double scaleX = getWidth() / (double) baseSize.width;
                double scaleY = getHeight() / (double) baseSize.height;

                for (Map.Entry<Component, Rectangle> entry : baseBounds.entrySet()) {

                    Component c = entry.getKey();
                    Rectangle r = entry.getValue();

                    int newX = (int) (r.x * scaleX);
                    int newY = (int) (r.y * scaleY);
                    int newW = (int) (r.width * scaleX);
                    int newH = (int) (r.height * scaleY);

                    c.setBounds(newX, newY, newW, newH);
                }

                revalidate();
                repaint();
            }
        });

        // 🔁 Recalcular bounds reales una vez pintado
        SwingUtilities.invokeLater(() -> {
            baseBounds.clear();
            for (Component c : getComponents()) {
                baseBounds.put(c, c.getBounds());
            }
        });
    }

    /**
     * Opens a directory chooser to select the download path.
     *
     * @param evt action event triggered by the browse button
     */
    private void jButtonBrowse2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBrowse2ActionPerformed
        JFileChooser chooser = new JFileChooser();

        // Carpeta por defecto donde normalmente está yt-dlp
        String userHome = System.getProperty("user.home");
        java.io.File defaultDir = new java.io.File(userHome, "AppData\\Local\\yt-dlp");
        if (!defaultDir.exists()) {
            defaultDir = new java.io.File(userHome); // fallback si no existe
        }
        chooser.setCurrentDirectory(defaultDir);

        chooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        chooser.setDialogTitle("Select yt-dlp executable");
        chooser.setFileFilter(new javax.swing.filechooser.FileNameExtensionFilter("Executable files", "exe"));

        if (chooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            jTextFieldYtDlp.setText(chooser.getSelectedFile().getAbsolutePath());
        }
    }//GEN-LAST:event_jButtonBrowse2ActionPerformed

    /**
     * Returns to the main application panel.
     *
     * @param evt action event triggered by the return button
     */
    private void jButtonBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBackActionPerformed
        JFrame topFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        if (topFrame instanceof Main mainFrame) {
            mainFrame.setContentPane(mainFrame.mainPanel);
            mainFrame.revalidate();
            mainFrame.repaint();
        }
    }//GEN-LAST:event_jButtonBackActionPerformed

